// - [ ] name 속성(prop)이 전달되면 "안녕! {name} 😊" 제목이 표시됩니다.
//   - 문서에 제목이 표시되는 지 확인합니다.
//   - 제목 내용이 "안녕! {name} 😊"인지 확인합니다.
// --------------------------------------------------------------------------

import { string } from 'prop-types';

Greet.propTypes = {
  name: string,
};

/**@type {({ name }: { name: string }) => JSX.Element} */
function Greet({ name }) {
  if (name) {
    return <h1>안녕! {name} 😊</h1>;
  }

  return <button type="button">로그인</button>;
}

export default Greet;
